package com.mvc.dto;

public class ReplyDTO {

	private String b_idx;
	private String repl_idx;
	private String id;
	private String repl_comment;
	
	public String getB_idx() {
		return b_idx;
	}
	public void setB_idx(String b_idx) {
		this.b_idx = b_idx;
	}
	public String getRepl_idx() {
		return repl_idx;
	}
	public void setRepl_idx(String repl_idx) {
		this.repl_idx = repl_idx;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getRepl_comment() {
		return repl_comment;
	}
	public void setRepl_comment(String repl_comment) {
		this.repl_comment = repl_comment;
	}
	
	
}
